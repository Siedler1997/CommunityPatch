Raid1_DELAY 		=       0
Raid1_COUNTER 	        =	Raid1_DELAY

---------------------------------------------------------------------------------------------------------------------------

setupSpawnRaid1 = function()


	StartJob("TimerRaid1")

	end


---------------------------------------------------------------------------------------------------------------------------	
--
--	JOB: "TimerRaid1"
--
---------------------------------------------------------------------------------------------------------------------------	
	-------------------------------------------------------------------------------------------------------------------
	Condition_TimerRaid1 = function()
	-------------------------------------------------------------------------------------------------------------------
		
                
                if Raid1_COUNTER > 0 and  bulache==0 and IsDead("Bulangii1") then

                   Raid1_COUNTER = Raid1_COUNTER - 1

                   return false

                	elseif Raid1_COUNTER==0   then 

			return true
                      
                                 
			end

		end


		
	-------------------------------------------------------------------------------------------------------------------
	Action_TimerRaid1 = function()
	-------------------------------------------------------------------------------------------------------------------

			local i = GetRandom(1,4)
				
            local pos = GetPosition("raidsp1")
			if CP_Difficulty == 0 then
				RaidAttack1 = AI.Entity_CreateFormation(2,Entities.CU_BanditLeaderSword1,0,8,pos.X,pos.Y,0,0,0,0)
			else
				RaidAttack1 = AI.Entity_CreateFormation(2,Entities.CU_BanditLeaderSword2,0,8,pos.X,pos.Y,0,0,CP_Difficulty,0)
			end
            SetEntityName(RaidAttack1, "Bulangii1")

			Attack("Bulangii1","attack"..i)


            Raid1_DELAY 		=       100
            Raid1_COUNTER 	        =	Raid1_DELAY
	        StartJob("TimerRaid1")

            return true

	end
